interface SidebarProps {
  userName: string
  userRole: string
}

export default function Sidebar({ userName, userRole }: SidebarProps) {
  const pathname = usePathname()
  const router = useRouter()

  async function handleSignOut() {
    const supabase = createClient()
    await supabase.auth.signOut()
    router.push('/login')
  }

  return (
    <aside className="hidden md:flex flex-col fixed left-0 top-0 bottom-0 w-56 bg-primary z-40">
      {/* Logo */}
      <div className="px-5 py-5 border-b border-white/10">
        <div className="text-white font-bold text-lg leading-tight">YoungLife RI</div>
        <div className="text-blue-300 text-xs mt-0.5">CRM</div>
      </div>

      {/* Nav links */}
      <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
        {navItems.map((item) => {
          const isActive =
            item.href === '/' ? pathname === '/' : pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-white/20 text-white'
                  : 'text-blue-100 hover:bg-white/10 hover:text-white'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}

        {/* Admin section */}
        {userRole === 'admin' && (
          <>
            <div className="pt-3 pb-1 px-3">
              <span className="text-blue-300 text-xs uppercase tracking-wider font-semibold">Admin</span>
            </div>
            {[
              { href: '/admin/users', label: 'Users' },
              { href: '/admin/settings', label: 'Settings' },
              { href: '/admin/imports', label: 'Import Log' },
            ].map((item) => (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                  pathname.startsWith(item.href)
                    ? 'bg-white/20 text-white'
                    : 'text-blue-100 hover:bg-white/10 hover:text-white'
                }`}
              >
                {item.label}
              </Link>
            ))}
          </>
        )}
      </nav>

      {/* User info */}
      <div className="px-4 py-4 border-t border-white/10">
        <div className="text-white text-sm font-medium truncate">{userName}</div>
        <div className="text-blue-300 text-xs capitalize">{userRole}</div>
        <button
          onClick={handleSignOut}
          className="mt-2 text-blue-300 hover:text-white text-xs transition-colors"
        >
          Sign out
        </button>
      </div>
    </aside>
  )
}
